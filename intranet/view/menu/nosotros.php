                                    <h3 class="text-center mt-5 mb-3 text-info">HISTORIA</h3>
                                    <div id="section1" class="">    
                                        <h5>1955 - 1959</h5>
                                        <p>
                                        Este centro comienza a funcionar como posta en el barrio Collico, a cargo de un practicante que atendía a pacientes con cuadros agudos, derivando según gravedad al hospital regional de Valdivia y también realizando tratamientos inyectables, curaciones y visitas a pacientes enfermos a domicilio a petición de la familia.
                                        </p>
                                    </div>
                                    <div id="section2" class=""> 
                                        <h5>1960 - 1962</h5>
                                        <p>Se traslada a una casona antigua al lado del cementerio municipal, en la cual trabajaba 1 enfermera, 2 practicantes, 3 auxiliares de enfermería. 1 chofer y 1 empleado de servicio. 
                                        las prestaciones se ampliaron, comenzándose a realizar también control de niño sano y administración de vacunas a una vasta población infantil. un equipo se trasladaba a atender 3 veces por semana a la parroquia del sector Collico. 
                                        </p>
                                    </div>        
                                    <div id="section3" class="">         
                                        <h5>1963 - 1964</h5>
                                        <p>Debido a la construcción de nuevos nichos para aumentar la capacidad del camposanto. la dependencia no puede continuar utilizándose para atención sanitaria y deben trasladarse frente a la iglesia preciosa sangre, en el sector corvi, en donde se atiende en una casa habitación destinada a los sacerdotes. se recibió ayuda de otros países, eeuu envía a miembros del cuerpo de paz, médicos y enfermeras a colaborar en la atención sanitaria de tan desbastada comunidad.  
                                        </p>
                                    </div>
                                    <div id="section4" class="">         
                                        <h5>1964</h5>
                                        <p>En octubre se traslada a sus actuales dependencias en picarte 2500 a una hermosa y moderna construcción donada por el gobierno norteamericano. lleva el nombre de consultorio materno - infantil gil de castro de acuerdo a las prioridades de salud de la época; el binomio madre - hijo. el trabajo de terreno y la promoción popular se constituyen en un pilar importante para la reconstrucción de esta zona del país tan afectada por el terremoto. 
                                        </p>
                                    </div>
                                    <div id="section5" class="">         
                                        <h5>1965</h5>
                                        <p>Se extiende la atención hacia el barrio Collico una vez por semana. se realizaba en la antigua sede de la cruz roja. el equipo estaba conformado por médico pediatra, enfermera, matrona y auxiliar de enfermería, entregándose principalmente acciones eminentemente preventivas.
                                        </p>
                                    </div>
                                    <div id="section6" class="">         
                                        <h5>1973</h5>
                                        <p>Después del 11 de septiembre de 1973 el establecimiento es intervenido y dirigido por uniformados con formación profesional en salud, quienes organizan la atención de acuerdo con los postulados de la junta de gobierno. 
                                        </p>
                                    </div>
                                    <div id="section7" class="">         
                                        <h5>1978</h5>
                                        <p> La declaración   de alma-ata marcó y cambió profundamente el quehacer en el consultorio.
                                            durante la conferencia internacional de aps, reunida en alma ata, con representantes de 134 naciones se conviene una solemne declaración en la que se insta a los gobiernos, agentes de salud y la comunidad a adoptar medidas urgentes para proteger y promover la salud de los ciudadanos del mundo.
                                        </p>
                                    </div>
                                    <div id="section8" class="">         
                                        <h5>1983</h5>
                                        <p> Los puestos de trabajos nuevos son llenados con personal adscrito al plan de expansión de recursos humanos (perh), llegando a constituir un 30% de la dotación. este grupo de funcionarias trabajó en condiciones muy inferiores a los contratados y constituyeron un invaluable aporte para las metas sanitarias de la década.
                                        </p>
                                    </div>
                                    <div id="section9" class="">         
                                        <h5>1987</h5>
                                        <p> Se realiza una ampliación y remodelación de la planta física, con fondos de desarrollo regional, para dar satisfacción a la creciente demanda derivada de una gran cantidad de población beneficiaria radicada en campamentos y a la fundación de nuevos conjuntos habitacionales, aledaños al consultorio. 
                                        </p>
                                    </div>
                                    <div id="section10" class="">         
                                        <h5>1988</h5>
                                        <p>Se verifica el traspaso del establecimiento a la ilustre municipalidad de Valdivia, transformándose en un consultorio de atención primaria integral, que abarca los 3 programas básicos de salud de la época (maternal, infantil y adulto). se establece una doble dependencia; de tipo administrativo a través del gobierno comunal y de tipo técnico a través del servicio de salud. 
                                        </p>
                                    </div>
                                    <div id="section11" class="">         
                                        <h5>1990</h5>
                                        <p>La fundación kellogg financia un proyecto de ampliación muy esperado por los trabajadores, consistente en sala de reuniones y comunitaria, vestuarios, oficinas de dirección y supervisión. continúa el crecimiento estructural y también de la planta funcionaria y las actividades comunitarias. 
                                        </p>
                                    </div>
                                    <div id="section12" class="">         
                                        <h5>1991</h5>
                                        <p>La fundación kellogg financia un proyecto de ampliación muy esperado por los trabajadores, consistente en sala de reuniones y comunitaria, vestuarios, oficinas de dirección y supervisión. continúa el crecimiento estructural y también de la planta funcionaria y las actividades comunitarias. 
                                        </p>
                                        <p>         
                                        la atención primaria de salud es inyectada de múltiples recursos y el nivel central propicia capacitación y muchos cambios en la mirada, basados en la declaración de alma ata "salud para todos los años 2000". 
                                        </p>
                                        <p>
                                        durante este año el consultorio fue seleccionado entre los 4 centros pilotos para implementar un sistema computacional por parte de la empresa santiaguina synapsis con fondos bid-minsal. el programa denominado manager permitió optimizar los procesos administrativos y estadísticos del establecimiento, siendo este la primera aproximación a la modernización informática de la aps.
                                        </p>
                                    </div>
                                    <div id="section13" class="">         
                                        <h5>1992</h5>
                                        <p>el director de ssv dr. rené guzmán, con una visión futurista y pionero implementa un programa de extensión horaria, en jornada vespertina, para atención médica, control de salud de pacientes crónicos, niño sano y maternal 
                                        posteriormente se suma el programa psicosocial, antecesor del actual programa de salud mental. en la búsqueda de mejorar la atención de las gestantes introduce la ecografía obstétrica de primer nivel, estrategia impulsada por el director servicio de salud valdivia, dr. rené guzmán serani y siendo la primera matrona ecografista la srta. viviana palma icarte.
                                        </p>
                                    </div>
                                    <div id="section14" class="">         
                                        <h5>1995</h5>
                                        <p>tras una larga lucha de la entonces coordinadora de trabajadores de salud municipal, se promulga la ley 19.378 estatuto de atención primaria, la que se comienza aplicar en enero 1996, mejorando las condiciones de trabajo y remuneraciones del personal del establecimiento. se forma el centro integrado del adulto mayor; quién postula a un proyecto, adjudicándose la construcción y habilitación de un espacio físico. </p>
                                    </div>
                                    <div id="section15" class="">         
                                        <h5>1996</h5>
                                        <p>se inicia el proceso de cambio de consultorio a centro de salud
                                            paralelamente la uach comienza a dictar la especialidad de medicina familiar.
                                            entonces un equipo básico de salud se traslada a la población san pedro (sector angachilla) a realizar atención en salud, en dependencias de la sede social. lo anterior impulsado por la dra. marcela traub muñoz, primera becaria de salud familiar de nuestro establecimiento.
                                        </p>
                                    </div>
                                    <div id="section16" class="">         
                                        <h5>1997</h5>
                                        <p>
                                        se realiza convenio con la comunidad san pedro y con el apoyo del laboratorio smith – klein se readecuan e implementan dos box de atención.
                                        gracias a proyecto innovador realizado por enfermera referente de programa infantil se pone en marcha la primera sala de estimulación del desarrollo sicomotor a nivel nacional a cargo de educadora diferencial.  
                                        se gana proyecto de promoción de salud denominado “unidos por una vida sana”, estrategia promocional que se instala en la comunidad a través de actividades dirigidas a niños, jóvenes y adultos.
                                        al alero de este proyecto se busca   potenciar la alianza entre el consultorio y la comunidad y también para representar el quehacer institucional en forma simbólica se crea el primer logo, cuya imagen de la mariposa sobre una hoja nos recuerda la belleza y fragilidad de la vida y la salud.
                                        esta tarea se le encomendó al periodista alejandro wagner seguel.</p>

                                        <p>también como fruto de este proyecto, se edita el informativo “el werkèn”, como medio difusor de actividades preventivo promocionlaes realizados en el cesfam y comunidad.</p>
                                    </div>
                                    <div id="section17" class="">         
                                        <h5>1998</h5>
                                        <p>
                                        se crea el 1° consejo de desarrollo local cuyos objetivos son colaborar con el diagnóstico participativo de la salud de la población, 
                                        informar a la comunidad sobre el funcionamiento del centro de salud y contribuir al compromiso social en el fortalecimiento de la promoción de la salud.</p> 

                                        <p>en el marco de las nuevas políticas y convenios suscritos por la confusam y el gobierno en lo relacionado a capacitación viajan a españa y e.e.u.u. los primeros becarios de salud familiar. 
                                        este proyecto permitió que en años sucesivos aproximadamente 20 funcionarios viajen a distintos lugares del mundo a conocer experiencias innovadoras y consolidadas, que han nutrido el proceso de cambio de consultorio a centro de salud. </p>
                                    
                                        <p>se da inicio al programa odontológico jefas de hogar en extensión horaria, lo que significa poder brindar atención integral a pacientes adultos.
                                    </p>
                                    </div>
                                    <div id="section18" class="">         
                                        <h5>1999</h5>
                                        <p>
                                        este año es muy significativo para los anales de la historia salubrista de la comuna pues se comienza el proceso de cambio de centro de salud a centro de salud familiar. 
                                        importante paso para la implementación de este modelo fue la redistribución de la planta física, comenzándose a trabajar en forma sectorial angachilla, antumalal, lafquen-mapu y petrohué (4 sectores de 12.000 personas aproximadamente).
                                        </p>
                                        <p>se inaugura el módulo dental, gran anhelo del programa para innovar en la atención odontológica de los usuarios y principalmente población escolar, aplicando la tècnica de 4 manos.
                                        </p>
                                    </div>
                                    <div id="section19" class="">         
                                        <h5>2000</h5>
                                        <p>
                                        la ministra de salud michelle bachelet jeria visita nuestro centro de salud y pone la primera piedra para la construcción del centro de salud familiar san pedro actual cesfam angachilla.
                                        </p>
                                        <p>este mismo año se impulsa la reforma de salud con el fortalecimiento de la aps,
                                        se instala oirs, se crea la línea 800 para atención de morbilidad, se instauran horarios escalonados y diferidos, se amplía la extensión horaria y se extiende atención a los días sábados.
                                        </p>
                                        <p>
                                        comienza la atención odontológica de urgencia en extensión horaria
                                        </p>
                                        <p>
                                        se mejora la resolutividad en aps, mediante la compra de servicios de las especialidades con mayor lista de espera como ej. otorrinolaringología, oftalmología e imagenología.
                                        </p>
                                        <p>
                                        se inicia el programa de mejoramiento del minsal, destacando la urgencia dental con cobertura provincial. el establecimiento es acreditado como centro de salud familiar por el ministerio de salud.
                                        </p>
                                    </div>
                                    <div id="section20" class="">         
                                        <h5>2001</h5>
                                    <p>dado que la infraestructura existente no reunía las condiciones para el desarrollo del nuevo modelo de atención, el equipo decide trabajar para conseguir la construcción de un nuevo consultorio, de esta forma se presenta por primera vez el proyecto de reposición del consultorio gil de castro iniciativa liderada por la dra. regina barra en conjunto con el director patricio hermosilla, la subdirectora elizabeth tobar, y la enfermera supervisora ariela bórquez; el que por distinta razones técnicas y metodológicas es recién aprobado el año 2004 en su cuarta versión revisada por la encargada de proyectos del desam mónica fuentealba. </p>
                                    
                                    <p> el establecimiento es certificado como centro de salud familiar.</p>

                                    <p>se inicia la actividad ortodoncia interceptiva, lo que permite atender en forma precoz anomalías dentomaxilares interceptables en la población infantil.</p>
                                    </div>
                                    <div id="section21" class="">         
                                        <h5>2002</h5>
                                    <p>se recibe acreditación de fonasa como establecimiento que adhiere a la carta de los derechos del paciente. </p>
                                    <p>mediante un proyecto pionero e innovador, con financiamiento municipal se instaura la atención domiciliaria a pacientes terminales y postrados en horario de 08:00 a 20:00 horas. para ello se contrató un equipo conformado por 2 enfermera y 2 técnico paramédico y se adquirió un vehículo que era conducido por sus integrantes.</p>
                                    <p>se realiza convenio con junaeb para la atención odontológica para la población escolar
                                    además, se inicia la atención integral a pacientes con depresión, por parte de un equipo multidisciplinario que incluye medicamentos, terapia e interconsultas a especialistas. 
                                    </p> 
                                    </div>
                                    <div id="section22" class="">         
                                        <h5>2003</h5>
                                    <p>se crea el servicio de atención primaria de urgencia (sapu) que comienza a funcionar el mes de mayo con el objetivo de acercar la atención a las personas y descongestionar la urgencia del hospital base valdivia. participa en esta gestión el dr. patricio rosas.</p>
                                    <p>en el marco de la implementación de la ley 19.813 se crea la asignación al desarrollo y estímulo al desempeño colectivo que beneficia a los funcionarios afectos al estatuto de atención primaria.</p>
                                    <p>se establece convenio para la realización del programa primera respuesta dirigida a adolescentes con consumo abusivo de drogas. </p>
                                    </div>
                                    <div id="section23" class="">         
                                        <h5>2004</h5>
                                    <p>junto con la celebración de los 40 años del centro se da a conocer la aprobación técnica del proyecto de reposición del consultorio gil de castro. </p>
                                    </div>
                                    <div id="section24" class="">         
                                        <h5>2005</h5>
                                    <p>con la entrada en vigencia de la nueva ley de autoridad sanitaria y la ley auge el centro forma parte del plan piloto auge (ges) con las primeras 5 patologías.</p>
                                    </div>
                                    <div id="section25" class="">         
                                        <h5>2006</h5>
                                    <p>se inaugura el centro comunitario de salud familiar en collico, con un equipo formado por médico, enfermera, matrona, técnico paramédico, administrativo y horas de apoyo de nutricionista y sicólogo. su primera coordinadora fue la e.u. sra. marcia obreque briones.</p>
                                    <p>el proyecto de reposición se prolongo por más de tres años. ya que constructora abandonó la obra.
                                    para la reanudación de la obra se debió realizar una serie de movilizaciones en conjunto con la comunidad representada por su presidente el sr héctor peña y la municipalidad de valdivia.
                                    </p>
                                    </div>
                                    <div id="section26" class="">         
                                        <h5>2008</h5>
                                    <p>el concejo municipal ante requerimiento de la comunidad, aprueba cambio de nombre para el nuevo centro de salud familiar, denominándolo como centro de salud familiar dr. jorge sabat gozalo, ex alcalde de la ciudad.</p>
                                    </div>
                                    <div id="section27" class="">         
                                        <h5>2009</h5>
                                    <p>el 16 de abril se inicia el traslado hacia las nuevas dependencias y el día 20 de abril se inaugura el nuevo edificio, ubicado en avenida picarte nº 2500, el cual se transforma en un aporte al desarrollo urbano y arquitectónico de la zona sur de valdivia. el edificio cuenta con 2 plantas calefaccionadas, a través de losa radiante y con avanzado sistema inyección y extracción de aire. el primer piso está destinado a la atención de usuarios, oficinas de información, farmacia, entrega de leche, some. en el segundo piso se encuentran las oficinas administrativas, informática, casino y 2 salas de reuniones. se crea el servicio de atención primaria de urgencia (sapu) que comienza a funcionar el mes de mayo con el objetivo de acercar la atención a las personas y descongestionar la urgencia del hospital base valdivia</p>
                                    <p>a poco andar de la puesta en marcha el centro de salud y su equipo deben enfrentar la gran pandemia por influenza h1n1, sorteando con éxito los desafíos epidemiológicos y las demandas de atención de la comunidad</p>
                                    <p>una vez superada la pandemia, se da inicio a la planificación estratégica del´cesfam, fijándose 3 objetivos estratégicos orientados al fortalecimiento del modelo salud familiar, potenciar el trabajo comunitario y la instalación de una cultura de calidad y seguridad para los usuarios.</p>
                                    <p>para el logro de los objetivos se modifica la estructura organizacional, relevando 5 áreas de gestión, el trabajo con las familias a cargo de los distintos sectores. los que conforman el consejo asesor de la dirección, junto al codelo y el comité paritario.</p>
                                    <p>en el mes de noviembre del 2009 se da inicio a la implementación del registro clínico electrónico rayén que permitió entre otros estandarizar la atención en aspectos clínicos y administrativos. para ello se dotó de computadoras para cada box y se incorpora a la carretera informática dl minsal </p>
                                    </div>
                                    <div id="section28" class="">         
                                        <h5>2010</h5>
                                    <p> en el mes de abril este establecimiento logra la primera certificación como” centro de salud de nivel superior”.</p>
                                    <p>como parte de una estrategia local impulsada por funcionarios y comunidad, financiada por el minsal -y solo por un año-  comienza a funcionar el servicio de urgencia odontológica de cobertura comunal para abordar atención de urgencia, en el mismo horario de sapu médico. dado que el financiamiento del ministerio no tuvo continuidad, la municipalidad y su concejo aprueban la reconversión de fondos municipales que junto a la firma de un convenio con el servicio de salud valdivia logran revertir el término de la atención de urgencia.</p>
                                    <p>en el mismo ámbito de la salud bucal y con aportes de junaeb se instala clínica odontológica completa en escuela f9, fernando santiván.</p>
                                    
                                    </div>
                                    <div id="section29" class="">         
                                        <h5>2011</h5>
                                    <p>en octubre se incorporó un nuevo sector llamado llancahue, quedando conformado el cesfam por cuatro sectores, más el cecosf de collico. </p> 
                                    <p>recibe el nombramiento de centro de salud familiar de excelencia ocupando el 15avo lugar entre 33 centros de salud seleccionados.</p>
                                    <p>la iniciativa partió en julio del 2011, y de las 156 postulaciones, sólo 40 pasaron la fase de preselección en base a criterios como gestión clínica, gestión de recursos humanos, trabajo para la comunidad y gestión financiera. tras ello, fueron claves las opiniones de la comunidad por la vía de tres encuestas para medir la percepción de los usuarios, en agosto, septiembre y noviembre</p>
                                    <p>el recinto fue elegido entre los 33 mejores a nivel nacional, según encuestas de satisfacción aplicadas a usuarios durante el 2011, y cuyos resultados se dieron a conocer en enero pasado. el cesfam dr. sabat ocupa la posición número 15, y es el único de la región de los ríos en recibir la distinción.</p>
                                    <p>la selección de los consultorios de excelencia se hizo en base a dos criterios: aquellos que presentaron los mejores índices en la tercera encuesta realizada, o que registraron las mayores alzas al comparar la primera y última evaluación.</p>
                                    <p>el servicio de salud le otorgará un aporte de 80 millones de pesos al consultorio, los que deberán ser invertidos en proyectos para mejorar la calidad de la atención y la implementación de este centro.</p>
                                    </div>
                                    <div id="section30" class="">         
                                        <h5>2012</h5>
                                    <p>se reedita el antiguo boletín del consultorio gil de castro, “el werkén” (mensajero), bajo la dirección de la periodista verónica ruiz.</p>    
                                    </div>
                                    <div id="section31" class="">         
                                        <h5>2013</h5>
                                    <p>en el mes de octubre se produce un principio de incendio que compromete el entretecho de los sectores petrohué y lafquen mapu. se suspende la atención y evacúa el establecimiento por el riesgo que implica para los usuarios.</p>    
                                    </div>
                                    <div id="section32" class="">         
                                        <h5>2014</h5>
                                    <p>se celebran las bodas de oro con multiples eventos</p>    
                                    </div>
                                    <div id="section33" class="">         
                                        <h5>2015</h5>
                                    <p>cesfam de excelencia, en octubre comienza el agendamiento telefonico provisto por empresa gda.</p>    
                                    </div>
                                    <div id="section34" class="">         
                                        <h5>2016</h5>
                                    <p>se formaliza alianza con uach para creacion de nuevo cecosf, se amplia dependencias cecosf: farmacia y pnac.</p>    
                                    </div>
                                    <div id="section35" class="">         
                                        <h5>2017</h5>
                                    <p>cesfam logra 1ª acreditacion con un 81% de cumplimiento. se suman a cesfam medicos extranjeros e inmigrantes principalmente haitianos y venezolanos.</p>    
                                    </div>
                                    <div id="section36" class="">         
                                        <h5>2018</h5>
                                    <p>se amplia cecosf, comienzan las tertulias en la comunidad.</p>    
                                    </div>
                                    <div id="section37" class="">         
                                        <h5>2019</h5>
                                    <p>se construyen 6 box multiproposito lo que aumenta la disponibilidad de espacios de atencion sectoriales.
                                    se licita nueva empresa (hora facil) para agendamiento telefonico la que considera mejoras sustanciales respecto de la anterior. se continúa trabajando en proyecto cecosf asitencial docente en alianza con uach.   
                                    a raiz del estallido social de octubre, los funcionarios adhieren   a la paralizacion de actividades (manteniendo turnos eticos) en pro de reivindicaciones sociales largamente anheladas por las personas.
                                    se realizan cabildos con alta participacion ciudadana en el cesfam. 
                                    </p>    
                                    </div>
                                    <div id="section38" class="">         
                                        <h5>2020</h5>
                                    <p> se termina la construcción del tan anhelado cecosf mulato gil de castro, que se emplaza en el ex establecimiento educacional del mismo nombre, como gestor del modelo fue seleccionado el profesional fredd guarda catalán.</p>    
                                    <p>el 05 de febrero se promulga el decreto n°4 alerta sanitaria por el período que se señala y otorga facultades extraordinarias que indica por emergencia de salud pública de importancia internacional (espii) por brote del nuevo coronavirus.</p>
                                    <p>se crea la unidad de bioseguridad, pionera en la región y que se instala en los demás cesfam por considerarse una buena práctica.</p>
                                    <p>el cesfam se reorganiza tanto en relación a los recursos humanos como físicos para dar respuesta de mejor forma a los múltiples requerimientos de usuarios internos y externos.</p>
                                    <p>se instala la entrega domiciliaria de medicamentos e insumos, productos pnac y pacam.</p>
                                    <p>por gestión del codelo, ministerio de bienes nacionales, hace traspaso de las dependencias de la ex pdi a la ilustre municipalidad de valdivia, instalandose allí algunos dispositivos del cesfam dr. sabat como por ej. elige vida sana, más ama, sala de estimulación.</p>
                                    </div>
                                    <div id="section39" class="">         
                                        <h5>2021</h5>
                                    <p>se comienza vacunación covid 19, estableciendose varios puestos para satisfacer la alta demanda que esta concita en la comunidad. </p>   
                                    <p>aumenta mucho la solicitud de inscripción, principalmente por la migración de usuarios desde las isapres.
                                            se remodelan los módulos dentales, transformándose en boxes dentales de acuerdo a nuevos requerimientos normativos.
                                            se amplian los espacios destinados a colación del personal para lo cual construye una sala en la planta alta.
                                    </p> 
                                    </div>
                                    <div id="section40" class="">         
                                        <h5>2022</h5>
                                    <p>se obtiene la autorización sanitaria de cecosf mulato y se comienza a trabajar en la creación de un nuevo sector denominado valdivia sur.
                                    se retorna progresivamente a la presencialidad.
                                    </p>    
                                    </div>
